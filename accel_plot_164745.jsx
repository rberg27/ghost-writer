function parseCSV(csv) {
  const lines = csv.trim().split("\n");
  const rows = [];
  for (let i = 1; i < lines.length; i++) {
    const parts = lines[i].split(",");
    if (parts.length < 5) continue;
    rows.push({
      timestamp: parts[0],
      elapsed: parseFloat(parts[1]),
      x: parseFloat(parts[2]),
      y: parseFloat(parts[3]),
      z: parseFloat(parts[4]),
    });
  }
  return rows;
}

function computeDeltas(data) {
  return data.map((row, i) => {
    if (i === 0) return { ...row, dx: 0, dy: 0, dz: 0 };
    var prev = data[i - 1];
    return {
      ...row,
      dx: parseFloat((row.x - prev.x).toFixed(4)),
      dy: parseFloat((row.y - prev.y).toFixed(4)),
      dz: parseFloat((row.z - prev.z).toFixed(4)),
    };
  });
}

